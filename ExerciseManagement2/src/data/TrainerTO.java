package data;

public class TrainerTO {
	private String tserial;
	private String id;
	private String tname;
	private String tcontact;
	
	public String getTserial() {
		return tserial;
	}
	public String getId() {
		return id;
	}
	public String getTname() {
		return tname;
	}
	public String getTcontact() {
		return tcontact;
	}
	public void setTserial(String tserial) {
		this.tserial = tserial;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public void setTcontact(String tcontact) {
		this.tcontact = tcontact;
	}	
}
