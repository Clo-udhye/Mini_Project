
public class UserTO {
	
	private String id;
	private String password;
	private String type;
	
	public String getId() {
		return id;
	}
	public String getPassword() {
		return password;
	}
	public String getType() {
		return type;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public void setType(String type) {
		this.type = type;
	}
}
